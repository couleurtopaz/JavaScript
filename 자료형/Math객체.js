// 절댓값 (Absolute Number) : Math.abs(x) 를 하면 x의 절댓값이 리턴
console.log(Math.abs(-10)); // -> 10 출력

// 최댓값 (Maximum) or 최솟값 (Minimum)
console.log(Math.max(2, -2, 5, 0)); // -> 5
console.log(Math.min(2, -2, 5, 0)); // -> -2

// 거듭게곱 (Exponentiation) : Math.pow(2, 3) 을 하면 x의 y승의 결과값이 리턴
console.log(Math.pow(2, 3)); // -> 8
    
// 제곱근 (Square Root) : Math.sqrt(x) 를 하면 x 의 제곱근이 리턴
console.log(Math.sqrt(25));

// 반올림 (Round) : Math.round(x)
console.log(Math.round(2.3));

// 난수 (Random) : 0 이상 1 미만의 랜덤의 값이 리턴
console.log(Math.random());
    



